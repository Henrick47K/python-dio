#INT PARA FLOAT

preco = 10
print(preco)

preco = float(preco)
print(preco)

preco = 10 / 2
print(preco)

#FLOAT PARA INT

preco = 10.30
print(preco)

preco = int(preco)
print(preco)

#CONVERSÃO POR DIVISÃO

preco = 10
print(preco)

print(preco / 2)

print(preco // 2)

#NÚMERICO PARA STRING

preco = 10.50
idade = 28

print(str(preco))

print(str(idade))

texto = f"idade {idade} preco {preco}"
print(texto)

#STRING PARA NÚMERO

preco = "12.40"
idade = "25"

print(float(preco))

print(float(idade))